#pragma once

namespace jaccen
{
    namespace math
    {
        template <typename T>
        T clamp(const T& value, const T& min, const T& max)
        {
            if (value < min)
            {
                return min;
            }
            if (value > max)
            {
                return max;
            }
            return value;
        }
    }
}